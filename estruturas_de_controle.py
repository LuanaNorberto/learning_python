# Exemplo de condicional e loop

for i in range (5):
    if i % 2 == 0:
        print(f'{i} é par')
    else:
        print(f'{i} é impar')
        
        # range cria uma sequência de 0 a 5 (0,1,2,3,4)e verifica o que é par e ímpar. 
        # a porcentagem é uma divisão, verifica se i = 0 é divisível por 2, neste caso,0 é par.